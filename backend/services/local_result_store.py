"""
Temporary local-result handoff store for strict browser-local file mode.

The server keeps generated bytes only long enough for the browser to claim them
and persist them into IndexedDB/OPFS. This is intentionally process-local.
"""
import io
import threading
import time
import uuid
from dataclasses import dataclass
from typing import Optional


@dataclass
class LocalResult:
    id: str
    data: bytes
    content_type: str
    filename: str
    created_at: float
    expires_at: float
    project_id: str
    page_id: Optional[str] = None
    version_number: Optional[int] = None


class LocalResultStore:
    def __init__(self, ttl_seconds: int = 3600):
        self.ttl_seconds = ttl_seconds
        self._items: dict[str, LocalResult] = {}
        self._lock = threading.Lock()

    def put_image(self, image, project_id: str, page_id: str, version_number: int,
                  image_format: str = "PNG") -> LocalResult:
        result_id = str(uuid.uuid4())
        normalized_format = (image_format or "PNG").upper()
        content_type = "image/png" if normalized_format == "PNG" else f"image/{normalized_format.lower()}"
        ext = "png" if normalized_format == "PNG" else normalized_format.lower()

        buf = io.BytesIO()
        image.save(buf, format=normalized_format)
        now = time.time()
        result = LocalResult(
            id=result_id,
            data=buf.getvalue(),
            content_type=content_type,
            filename=f"{page_id}_v{version_number}.{ext}",
            created_at=now,
            expires_at=now + self.ttl_seconds,
            project_id=project_id,
            page_id=page_id,
            version_number=version_number,
        )
        with self._lock:
            self._cleanup_locked(now)
            self._items[result_id] = result
        return result

    def put_bytes(self, data: bytes, project_id: str, filename: str,
                  content_type: str, page_id: Optional[str] = None) -> LocalResult:
        result_id = str(uuid.uuid4())
        now = time.time()
        result = LocalResult(
            id=result_id,
            data=data,
            content_type=content_type,
            filename=filename,
            created_at=now,
            expires_at=now + self.ttl_seconds,
            project_id=project_id,
            page_id=page_id,
        )
        with self._lock:
            self._cleanup_locked(now)
            self._items[result_id] = result
        return result

    def get(self, result_id: str) -> Optional[LocalResult]:
        now = time.time()
        with self._lock:
            self._cleanup_locked(now)
            result = self._items.get(result_id)
            if result and result.expires_at >= now:
                return result
            return None

    def _cleanup_locked(self, now: float) -> None:
        expired = [key for key, item in self._items.items() if item.expires_at < now]
        for key in expired:
            self._items.pop(key, None)


local_result_store = LocalResultStore()
